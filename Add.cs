using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calci
{
    class Add
    {
        public static int add(int first,int second)
        {
            return first + second;
        }

        public static float add(float first,float second)
        {
            return first * second;
        }
    }
}
