using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calci
{
    internal class Sub
    {
        public static int sub(int first, int second)
        {
            return first - second;
        }
        public static float sub(float a, float b)
        {
            return a - b;
        }

    }
}
